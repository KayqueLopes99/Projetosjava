  var elementoTabela = document.getElementById("tabelaJogadores");
  
  exibirNaTela();
  
  function exibirNaTela() {
    elementoTabela.innerHTML = `
        <tr>
            <td>${pikachu.nome}</td>
            <td>${pikachu.vitoria}</td>
            <td>${pikachu.empate}</td>
            <td>${pikachu.derrota}</td>
            <td>${pikachu.pontos}</td>
            <td><button onClick="adicionarVitoria(pikachu)">Vitória</button></td>
            <td><button onClick="adicionarEmpate(pikachu)">Empate</button></td>
            <td><button onClick="adicionarDerrota(pikachu)">Derrota</button></td>
        </tr>
        
    `;/*adicionar outros pokémons em breve.*/
  }
  
  function adicionarVitoria(jogador) {
    jogador.vitoria++;
    jogador.pontos = jogador.pontos + 3;
    exibirNaTela();
  }
  
  function adicionarEmpate(jogador) {
    jogador.empate++;
    jogador.pontos++;
    exibirNaTela();
  }
  
  function adicionarDerrota(jogador) {
    jogador.derrota++;
    exibirNaTela();
  }
  